<strong style="color: #C60">1. 如果我的电脑网络或电脑出问题了会发生什么事？</strong><br/>

金狮国际娱乐场的软件系统监视着游戏运作的每一步。 当网络连接重新接上或是电脑被重新启动，然后玩家重新登录金狮国际网页，游戏停留在游戏被中断的地方可让玩家继续完成它，除了'实况赌博娱乐场'。因为'实况赌博娱乐场'每一场赌局时间都是现场而且是真实 的。一旦玩家确定了他们的下注，下注花费会直接进入玩家的帐户不管网络连接失败。赌局的结果仍然可以到' 账户'里面的'报告' 查询。<br/>

<strong style="color: #C60">2. 如果我要投诉，跟谁联络？</strong><br/>

在投诉的情况下，请给娱乐场发电子邮件。 金狮国际娱乐场可以帮助更快地解决问题，提到的内容和情况越多越好。 我们会做出调查然后尽快解决问题。 <br/>

<strong style="color: #C60">3. 我怎么肯定游戏都是公平的，真实地呈现了'真实现场'游戏？</strong><br/>

金狮国际所有的产品皆由菲律宾政府第一卡格杨经济特区(FCLRC)授权和监管。所有技术和操作标准都符合相关要求。<br/>

<strong style="color: #C60">4. 我怎样开始玩？</strong><br/>

在注册为我站的有效用户并成功存款后，点击进入游戏，即可进入游戏大厅。<br/>

<strong style="color: #C60">5. 玩时需要的电脑要求和软件是什么？</strong><br/>

PIII-800以上<br/>
IE 6.0或Firefox浏览器<br/>
Adobe Flash Player 8.0以上<br/>
稳定和快速的网络连接<br/>
多媒体扬声器(随意的)<br/>
显示器最少1024 x 768的分辨率<br/>

<strong style="color: #C60">6. 游戏的结果是怎样决定的？</strong><br/>

系统采用的是任意数字生产器(RNG) 保证每一个游戏的结果都是任意的。<br/>

<strong style="color: #C60">7. 用户名和密码都是高度机密的吗？</strong><br/>

是<br/>

<strong style="color: #C60">8. 我不记得密码了，我应该怎么做？</strong><br/>

按 '遗失密码 '图 标，填充表格再递交表格，娱乐场会帮你登记一个新的密码，再寄给你一个户口启动密码。或者和我们的客服人员联系<br/>

<strong style="color: #C60">9. 我的网络有时掉线，我应该怎么做？</strong><br/>

请查检您的网络接连稳定性，或者联系您的网络提供商取得帮助。<br/>

<strong style="color: #C60">10. 我怎样知道关于游戏的更多东西？</strong><br/>

当你在游戏里，选择'规则' 图案，就会打开游戏的规则和付钱表格的内容。<br/>

<strong style="color: #C60">11. 我认为我赢了几把，但我没有拿到钱，有没有任何方法可以察到结果和我有没有收到奖金？</strong><br/>

玩家可以按'大厅'键看他们的游戏历史，然后再选择'账户' 键，然后按'报告'键 玩家输入游戏的时间和日期，系统就会显示出一个报告， 此报告可以打印出来。要解释结果，玩家需要回到游戏的规则里，那里有所有的资料。<br/>

<strong style="color: #C60">12. 我怎么样更改我的个人资料？</strong><br/>

玩家可以登录娱乐场后更改自己的资料，然后选择"账户"菜单连接，再选择"用户资料"连接作出恰当的更改。<br/>

