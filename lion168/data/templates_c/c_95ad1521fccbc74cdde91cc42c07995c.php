  盈利及支付：</p>
<p align="left">1. 我是如何赚取佣金的？</p><br>
<p align="left">当您将我们的推广链接嵌入到您的网站上后，浏览者通过点击您网站上的(金狮国际)宣传条幅或文字链接或通过电子报等方式进入(金狮国际)注册网页并注册成为客户玩家，同时定义为活跃客户玩家，您就可以从您介绍的玩家所生成的净盈利中赚取客户酬金。您的收益来自于您所介绍的玩家，您介绍的玩家越多您的客户酬金就越高。 <br /><br>
  2. 什么叫&ldquo;活跃客户玩家&rdquo;？</p><br>
<p align="left">活跃客户玩家就是成功注册一个金狮国际玩家账户，并使用任何一种付款方式存款并进行有效投注，金狮国际就会将他们定义为活跃客户玩家。 <br /><br>
  3. 我的合营酬金是如何计算的？</p><br><br>
<p align="left">客户酬金是按照我们在体育博彩和真人娱乐场里从您所介绍的玩家中所生成的纯净利的<strong>30%-50%+</strong>的收益来计算的。(请参阅合营计划规则与条例的第三条).<strong> </strong><br /><br>
  4. 有最低提款要求吗？</p><br>
<p align="left">我们的客户酬金支付要求是：每一个金狮国际合营账户必须最少有5位活跃玩家，并且每月最低支付额为人民币500元。您达到以上要求即可申请提取客户酬金。 <br /><br>
  5. 我如何得到佣金？</p><br>
<p align="left">一旦您符合了取款要求，请您通过您的(金狮国际)玩家账户提取客户酬金。（请您确保您注册的玩家账户名与您的合营账户名一致）。您可以通过您的玩家账户申请客户酬金银行转账。（衷心提醒您：请您确保金狮国际玩家账户中的信息是真实有效的，以避免不必要的提款延迟，并请确认您的金狮国际玩家账户的注册姓名与您的银行开户名一致）。感谢您的支持和配合！<br>
  建议：您在注册合营账户前先注册金狮国际玩家账户，然后在注册合营账户时将玩家账号填入客户账号空格内，这样可以保持账号的一致性，以便于您今后的提款。<br />
  再次强调：您的玩家账户不可以通过任何的合营链接来注册（包括您的推广链接），也就是说您不可以把自己作为任何合营客户的下线玩家。否则您的注册账户将被删除。 <br /><br>
  6. 我多久可以拿到佣金？</p><br>
<p align="left">(金狮国际)合营部将在每个月月初的首七个工作日内核算合营客户上个月的酬金，核算结束后自动划到合营客户的玩家账户中，然后您就可以申请提款了。<br />
  请注意：银行转账时长取决于相关银行的处理时间。 <br /><br>
  7. 我的佣金可以付款到其他人的账户里吗?</p><br>
<p align="left">为了您的资金安全，我们只会付款到您本人的账户内，不便之处敬请见谅。 <br /><br>
  8.我可以从本人的存款及投注中赚取佣金吗？ </p><br>
<p align="left">合营客户是不可以从自己账户投注中赚取酬金的，如果您把您自己注册成为下线玩家，很抱歉，那么您的注册账户将被删除。 <br /><br>
  9. 你们会因为放置在我网站的广告条而付款给我吗？</p><br>
<p align="left">您将只在您介绍的会员为公司生成的净利润的基础上赚取酬金，我们不会因为您个人网站上的有我们广告或由此产生的点击率而付款给您。 <br /><br>
  10. 为什么我的合营账户余额显示为负值？</p><br>
<p align="left">如果您的账户报告显示余额为负值，说明您介绍的会员给公司带来的了亏损，换句话就是您介绍的会员总体赢的要比输的多。然而，您也不需要偿还这些亏损，当您介绍的玩家开始使公司盈利的时候，您自然又开始赚钱了。另外，基于具体的情况，如果您的账户余额在5个月内连续累积都是负值，我们将为您清零，这样您就可以重新开始新的累积了。 <br /><br>
