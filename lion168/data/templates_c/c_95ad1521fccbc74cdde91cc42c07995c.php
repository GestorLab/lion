<p align="left"> <strong>2. 我该如何创建/添加 (金狮国际) 合营的宣传条幅及文字链接？ </strong></p><br>
<p align="left">1）在我们的合营网页登录您的账号。<strong> </strong><br />
  2）点击进入&ldquo;宣传条幅&rdquo;您可以选择多种多样适合您的网站的宣传条幅及文字链接。<strong> </strong><br />
  3）您按照提示来创建宣传条幅代码，并嵌入到您的网站的合适位置。<strong> </strong><br />
  请注意：为了配合您的网站风格，选择宣传条幅时请先确定尺寸，语言（英文、中文简体、中文繁体），类别（体育博彩、真人娱乐场）。</p> <br />
<p><strong> 3. 为什么我要创建更多的链接？ </strong></p><br />
<p align="left">您的网站拥有越多的(金狮国际)链接，就有可能为(金狮国际)带来更多您介绍的玩家，您介绍的玩家越多就意味着您有机会得到更广的利润空间。</p><br />
<p><strong>4. 我可以从 (金狮国际) 获取多少个链接？</strong></p><br>
<p align="left">链接是没有限制的，您网站上拥有越多的链接，浏览者通过您网站登录 (金狮国际)的机会就越大，就会产生更多的潜在玩家和更多的收益。</p><br />
<p><strong>5. 我可以在我的网站使用(金狮国际)标志及相关内容吗？</strong></p>
<p align="left">一旦您加入我们的合营计划后，您将只被准许使用我们提供的宣传条幅及文字链接，在未得到 (金狮国际)书面允许的情况下，您不得对相关内容进行任何修改。如果您不明白您的使用权限或您想在您的网站上更改有关(金狮国际)宣传内容，请发送邮件至cs@lion68.com或QQ联系我们的合营团队。</p><br />
<p><strong>6. 我应该把文字链接及广告图片放在我网站上的什么位置？ </strong></p><br>
<p align="left">您应该把宣传条幅和文字链接放置在您的网站主页或其他高访问量的页面的显要位置，(金狮国际)在您网站上越明显，越多的访客就会认识并注册成为(金狮国际)玩家。您也可以联系我们的合营客户专员共同研究最佳方案。</p><br />
<p><strong>7. 如果我有多个网站呢？我所有的网站都会是相同的合营用户名吗？ </strong></p><br>
<p align="left">您当然可以使用多个网站一起宣传我们的品牌。我们强烈建议您对您全部的网站都添加管理追踪链接</p><br />
<p><strong>8. 您怎么知道玩家是从我的网站介绍的？</strong></p><br>
<p align="left">浏览者通过您的网站链接至(金狮国际)网站 ，会通过&ldquo;cookie&rdquo;程序记录下来，这就意味着当这些潜在客户由于某种原因没有及时注册，(金狮国际)仍将保留该部分信息。在有效期内如果浏览者注册成功，我们仍将该玩家归为您的客户玩家。 </p><br />
